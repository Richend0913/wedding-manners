def generate_sitemap(guides):
    """Generate sitemap.xml."""
    urls = f"""  <url>
    <loc>{BASE_URL}/</loc>
    <lastmod>{NOW}</lastmod>
    <changefreq>weekly</changefreq>
    <priority>1.0</priority>
  </url>\n"""
    for g in guides:
        urls += f"""  <url>
    <loc>{BASE_URL}/guides/{g["slug"]}.html</loc>
    <lastmod>{NOW}</lastmod>
    <changefreq>monthly</changefreq>
    <priority>0.8</priority>
  </url>\n"""
    return f"""<?xml version="1.0" encoding="UTF-8"?>
<urlset xmlns="http://www.sitemapindex.org/schemas/sitemap/0.9">
{urls}</urlset>"""


def generate_robots():
    """Generate robots.txt."""
    return f"""User-agent: *
Allow: /
Sitemap: {BASE_URL}/sitemap.xml"""


def main():
    base = os.path.dirname(os.path.abspath(__file__))
    guides_dir = os.path.join(base, "guides")
    os.makedirs(guides_dir, exist_ok=True)
    os.makedirs(os.path.join(base, "css"), exist_ok=True)

    # Generate guide pages
    for guide in GUIDES:
        filepath = os.path.join(guides_dir, f"{guide['slug']}.html")
        with open(filepath, "w", encoding="utf-8") as f:
            f.write(generate_guide_page(guide, GUIDES))
        print(f"  Generated: guides/{guide['slug']}.html")

    # Generate index
    with open(os.path.join(base, "index.html"), "w", encoding="utf-8") as f:
        f.write(generate_index(GUIDES))
    print("  Generated: index.html")

    # Generate sitemap
    with open(os.path.join(base, "sitemap.xml"), "w", encoding="utf-8") as f:
        f.write(generate_sitemap(GUIDES))
    print("  Generated: sitemap.xml")

    # Generate robots.txt
    with open(os.path.join(base, "robots.txt"), "w", encoding="utf-8") as f:
        f.write(generate_robots())
    print("  Generated: robots.txt")

    print(f"\nTotal guides generated: {len(GUIDES)}")
    print("Site generation complete!")


if __name__ == "__main__":
    main()
