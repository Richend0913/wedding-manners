def build_affiliate_html(items):
    """Generate affiliate box HTML for given items."""
    if not items:
        return ""
    html_parts = []
    for item in items:
        if item not in AFFILIATE_LINKS:
            continue
        link = AFFILIATE_LINKS[item]
        html_parts.append(f"""
    <div class="affiliate-box">
        <h4>{link['desc']}</h4>
        <p>結婚式の準備に必要な{item}をお探しなら、以下のリンクからどうぞ。</p>
        <div class="affiliate-links">
            <a href="{link['amazon']}" target="_blank" rel="nofollow noopener" class="btn-amazon">Amazonで探す</a>
            <a href="{link['rakuten']}" target="_blank" rel="nofollow noopener" class="btn-rakuten">楽天市場で探す</a>
        </div>
    </div>""")
    return "\n".join(html_parts)


def build_jsonld_article(guide):
    """Build JSON-LD structured data for an article."""
    return json.dumps({
        "@context": "https://schema.org",
        "@type": "Article",
        "headline": guide["title"],
        "description": guide["description"],
        "author": {"@type": "Organization", "name": SITE_TITLE},
        "publisher": {
            "@type": "Organization",
            "name": SITE_TITLE,
        },
        "datePublished": NOW,
        "dateModified": NOW,
        "mainEntityOfPage": {
            "@type": "WebPage",
            "@id": f"{BASE_URL}/guides/{guide['slug']}.html"
        }
    }, ensure_ascii=False)


def build_jsonld_website():
    """Build JSON-LD for the website."""
    return json.dumps({
        "@context": "https://schema.org",
        "@type": "WebSite",
        "name": SITE_TITLE,
        "url": BASE_URL,
        "description": SITE_DESC,
        "publisher": {"@type": "Organization", "name": SITE_TITLE}
    }, ensure_ascii=False)


def build_toc(sections):
    """Build table of contents HTML."""
    items = ""
    for i, (heading, _) in enumerate(sections, 1):
        items += f'<li><a href="#section-{i}">{heading}</a></li>\n'
    return f"""<div class="toc">
    <h4>目次</h4>
    <ol>{items}</ol>
</div>"""


def build_sections_html(sections):
    """Build article sections HTML."""
    html = ""
    for i, (heading, content) in enumerate(sections, 1):
        html += f'<h2 id="section-{i}">{heading}</h2>\n{content}\n\n'
    return html


def get_related_guides(current_guide, all_guides, max_related=4):
    """Get related guides from same category."""
    same_cat = [g for g in all_guides if g["category"] == current_guide["category"] and g["slug"] != current_guide["slug"]]
    other = [g for g in all_guides if g["category"] != current_guide["category"] and g["slug"] != current_guide["slug"]]
    result = same_cat[:max_related]
    if len(result) < max_related:
        result += other[:max_related - len(result)]
    return result


def build_related_html(related):
    """Build related articles HTML."""
    if not related:
        return ""
    cards = ""
    for g in related:
        cards += f'<a href="{g["slug"]}.html" class="related-card">{g["title"]}</a>\n'
    return f"""<div class="related-articles">
    <h3>関連記事</h3>
    <div class="related-grid">{cards}</div>
</div>"""


